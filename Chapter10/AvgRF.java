public class AvgRF {

    public static void main (String[] args) {
        double April = 23.0;
        double May = 28.0;
        double June = 37.0;
        System.out.println("Rainfall in April: " + April);
        System.out.println("Rainfall in May  : " + May);
        System.out.println("Rainfall in June : " + June);
        System.out.println("Average Rainfall : " + ((April + May + June) / 3));
    }

}